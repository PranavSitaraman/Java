package Section6.ForLoopsDemo;

public class CountingDown {

    public static void main(String args[]) {
        
        for (int i = 10; i >= 1; i--) {

            System.out.print(i + " ,");
        }
        System.out.println("Blast off!");
        System.out.println("The End!");

    }
}
