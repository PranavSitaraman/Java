package Section5.IfElseDemo;

public class RelationalDemo {

    public static void main(String args[]) {

        int a = 10;
        int b = 20;

        System.out.println("a == b = " + (a == b));//checks for equality
        System.out.println("a != b = " + (a != b));
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("b >= a = " + (b >= a));
        System.out.println("b <= a = " + (b <= a));

    }

}
