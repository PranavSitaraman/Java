package Section5.IfElseDemo;


public class BooleanIfDriving {
    public static void main(String args[]) {
        String left = "museum";
        String straight = "gym";
        String right = "restaurant";
      
         if(left=="gym")
         {
             System.out.print(" Turn Left");
            
         }
         if(straight=="gym")
         {
             System.out.print(" Drive straight");
            
         }

        if(right=="gym")
         {
             System.out.print(" Turn Right");
            
         }
    }

    
}
